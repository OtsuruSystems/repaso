public class PrimitivosFlotantes {
    public static void main(String[] args) {
        float realFloat = 212f;
        System.out.println("Real float: "+realFloat);
        System.out.println("Float coresponde en byte a: "+Float.BYTES);
        System.out.println("float corresponde en bites a = "+Float.SIZE);
        System.out.println("Maximo valor: "+Float.MAX_VALUE);
        System.out.println("Minimo valor: "+Float.MIN_VALUE);

double  realDouble = 3.4028235e39;
        System.out.println("Real double: "+realDouble);
        System.out.println("Double coresponde en byte a: "+Double.BYTES);
        System.out.println("Double corresponde en bites a = "+Double.SIZE);
        System.out.println("Maximo valor: "+Double.MAX_VALUE);
        System.out.println("Minimo valor: "+Double.MIN_VALUE);

    }
}
